#define _CRT_SECURE_NO_WARNINGS
#include <iostream>
#include <cstring>
using namespace std;

int main()
{
	char str1[30] = "안녕! ";
	char str2[30] = "난 초코야.";
	char str3[30];

	cout << "str1의 길이 = " << strlen(str1) << endl;
	cout << strcat(str1, str2) << endl;
	strcpy(str3, str1), cout << str3 << endl;
	if (!strcmp(str1, str3))
		cout << "두 문자열은 동일합니다!" << endl;

	return 0;
}

